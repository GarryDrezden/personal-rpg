const STATUS_DEFAULT: Record<BossStatus, string> = {
  untouched: 'Босс ещё держит территорию.',
  noticed: 'Первые трещины появились.',
  weakened: 'Босс слабеет от удержанного маршрута.',
  broken: 'Его власть заметно ослабла.',
  sealed: 'Сезонная печать почти закрыта.',
};

function seasonBoss(
  seasonId: number,
  title: string,
  shortTitle: string,
  description: string,
  weaknessText: string,
  strengthText: string,
  sources: BossDef['progressSources'],
  flavorLine: string,
  icon: string,
): BossDef {
  return {
    id: `season_mini_${String(seasonId).padStart(2, '0')}`,
    layer: 'seasonMiniBoss',
    seasonId,
    title,
    shortTitle,
    description,
    weaknessText,
    strengthText,
    progressSources: sources,
    flavorLine,
    statusCopy: { ...STATUS_DEFAULT },
    icon,
  };
}

