export function getSeasonMiniBossByIndex(seasonIndex: number): BossDef {
  const clamped = Math.min(13, Math.max(1, seasonIndex));
  return SEASON_MINI_BOSSES.find((b) => b.seasonId === clamped) ?? SEASON_MINI_BOSSES[0]!;
}

export function getChapterBossByChapterId(chapterId: number): BossDef | undefined {
  return CHAPTER_BOSSES.find((b) => b.chapterId === chapterId);
}

export function getActBossByActId(actId: 'I' | 'II' | 'III'): BossDef | undefined {
  return ACT_BOSSES.find((b) => b.actId === actId);
}
